package loopdemo;

public class RightAngledTriangle
{	
	public static void main(String[] args)
	{
		int i,j,n=7;
		System.out.println("The Right Angled Triangle");
		for(i=0;i<n;i++)
		{
			for(j=0;j<=i;j++)
			{
				System.out.println(" *");
			}
			System.out.println(" ");
		}
			
	}
}
